# compare dois valores e informe qual é o mairo
#

primeiro_valor = input('Digite um valor: ')
segundo_valor = input('Digite outro valor valor: ')

if primeiro_valor > segundo_valor:
    print(
        f'o Primeiro valor = {primeiro_valor} é mairo que o segundo valor = {segundo_valor}')
else:
    print(
        f'o Segundo valor = {segundo_valor} é mairo que o primeiro valor = {primeiro_valor}')
